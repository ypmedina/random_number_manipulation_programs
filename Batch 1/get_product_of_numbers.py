#Prog04: Create a program that ask user to input 2 numbers. Print the product of the two numbers.
#Get user input, print the product of two numbers
user_inp1 = float(input("Please enter a number"))
user_inp2 = float(input("Enter another number"))

user_prod = user_inp1 * user_inp2
print(f"{user_prod} is the product of the two entered numbers")